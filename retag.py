ALL_RULES = MATERIAL_RULES + FACILITY_RULES + STRUCTURE_RULES + USE_RULES

def auto_tags(description):
    """descriptionからタグを自動生成"""
    result = set()
    for keywords, tag in ALL_RULES:
        for kw in keywords:
            if kw in description:
                result.add(tag)
                break
    return result

def process():
    with open("builds.csv", encoding="utf-8") as f:
        reader = csv.DictReader(f, restval="")
        fieldnames = reader.fieldnames
        rows = list(reader)
    # Noneキーがあれば削除（余分なカンマ対策）
    for r in rows:
        r.pop(None, None)

    stats = {"total": len(rows), "added": 0, "removed": 0}
    tag_added_counter = {}

    for r in rows:
        old_tags = [t.strip() for t in r["tags"].split("|") if t.strip()]
        # 1. サイズ系・ムード系タグを削除
        kept = [t for t in old_tags if t not in REMOVE_TAGS]
        removed = len(old_tags) - len(kept)
        stats["removed"] += removed

        # 2. descriptionから自動タグ付与
        new_tags = auto_tags(r["description"])
        # 既に含まれているものは除外
        new_tags -= set(kept)

        for t in new_tags:
            tag_added_counter[t] = tag_added_counter.get(t, 0) + 1
        stats["added"] += len(new_tags)

        # 3. 結合
        final = kept + sorted(new_tags)
        r["tags"] = "|".join(final)

    # 書き出し
    with open("builds.csv", "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    # レポート
    print(f"処理完了: {stats['total']}件")
    print(f"  削除タグ: {stats['removed']}個（サイズ系+ムード系）")
    print(f"  追加タグ: {stats['added']}個")
    avg = stats['added'] / stats['total']
    print(f"  平均追加: {avg:.1f}個/件")
    print()
    print("=== 追加されたタグTOP30 ===")
    for tag, cnt in sorted(tag_added_counter.items(), key=lambda x: -x[1])[:30]:
        print(f"  {tag}: {cnt}件")

    # タグ数の分布
    print()
    print("=== 処理後のタグ数分布 ===")
    from collections import Counter
    dist = Counter()
    for r in rows:
        n = len([t for t in r["tags"].split("|") if t.strip()])
        dist[n] += 1
    for n in sorted(dist):
        print(f"  {n}個: {dist[n]}件")

if __name__ == "__main__":
    process()
